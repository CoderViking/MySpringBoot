public class Apad {
    public String ap;
    public String apid;
    public String ctr;
    public String cty;
    public String apad;

    public String getAp() {
        return ap;
    }

    public void setAp(String ap) {
        this.ap = ap;
    }

    public String getApid() {
        return apid;
    }

    public void setApid(String apid) {
        this.apid = apid;
    }

    public String getCtr() {
        return ctr;
    }

    public void setCtr(String ctr) {
        this.ctr = ctr;
    }

    public String getCty() {
        return cty;
    }

    public void setCty(String cty) {
        this.cty = cty;
    }

    public String getApad() {
        return apad;
    }

    public void setApad(String apad) {
        this.apad = apad;
    }
}
